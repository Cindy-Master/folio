export function t(locale, key, vars) {
  let str = translations[locale]?.[key] || translations.en[key] || key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, v)
    }
  }
  return str
}

